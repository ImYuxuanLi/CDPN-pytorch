def Hessian(f1coeff, f2coeff, f3coeff, s):
    # % the vector of monomials is
    # % m = [ const ; s1^2 * s2 ; s1 * s2 ; s1 * s3 ; s2 * s3 ; s2^2 * s3 ; s2^3 ; ...
    # %       s1 * s3^2 ; s1 ; s3 ; s2 ; s2 * s3^2 ; s1^2 ; s3^2 ; s2^2 ; s3^3 ;   ...
    # %       s1 * s2 * s3 ; s1 * s2^2 ; s1^2 * s3 ; s1^3]
    # %

    # prepend additional element to params to make them quasi 1-based
    s = np.hstack(([0], s.reshape(1, -1)[0]))

    # deriv of m w.r.t. s1
    Hs1 = np.array([[0], [2 * s[1] * s[2]], [s[2]], [s[3]], [0], [0], [0], [s[3]**2], [1], [0], [0],
                    [0], [2 * s[1]], [0], [0], [0], [s[2] * s[3]], [s[2]**2], [2 * s[1] * s[3]],
                    [3 * s[1]**2]])

    # deriv of m w.r.t. s2
    Hs2 = np.array([[0], [s[1]**2], [s[1]], [0], [s[3]], [2 * s[2] * s[3]], [3 * s[2]**2], [0], [0],
                    [0], [1], [s[3]**2], [0], [0], [2 * s[2]], [0], [s[1] * s[3]],
                    [s[1] * 2 * s[2]], [0], [0]])

    # deriv of m w.r.t. s3
    Hs3 = np.array([[0], [0], [0], [s[1]], [s[2]], [s[2]**2], [0], [s[1] * 2 * s[3]], [0], [1], [0],
                    [s[2] * 2 * s[3]], [0], [2 * s[3]], [0], [3 * s[3]**2], [s[1] * s[2]], [0],
                    [s[1]**2], [0]])

    H = np.array([[f1coeff.T @ Hs1, f1coeff.T @ Hs2, f1coeff.T @ Hs3],
                  [f2coeff.T @ Hs1, f2coeff.T @ Hs2, f2coeff.T @ Hs3],
                  [f3coeff.T @ Hs1, f3coeff.T @ Hs2, f3coeff.T @ Hs3]])

    return H.reshape(3, 3)


