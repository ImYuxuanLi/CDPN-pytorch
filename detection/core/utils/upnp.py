def setup_action_matrix_symm(M):
    M1 = np.zeros((8, 24))

    for (r, c), rs in M1_oplist:
        value = 0
        for factor, r_, c_ in rs:
            value += factor * M[r_, c_]
        M1[r, c] = value

    for r, c, value in M1_1oplist:
        M1[r, c] = value

    # First block elimination (upper [0-7:0-24))
    block = np.matmul(np.linalg.inv(M1[:7, :7]), M1[:7])
    M1 = np.vstack((block, M1[7]))

    # Some more cancellation in column 10
    for i in range(6):
        factor = M1[i, 10] / M1[7, 10]
        M1[i] -= factor * M1[7]

    M2 = np.zeros((141, 149))
    for line in M2_oplist:
        for r, c, r_, c_ in line:
            M2[r, c] = M1[r_, c_]

    # Now do Gauss-Jordan elimination with exit condition of 121
    elimination = gauss_jordan(M2, 121)

    # from scipy.linalg import lu
    # pl, u = lu(elimination, permute_l=True)
    # np.savetxt('U.txt', u[:, 100:], fmt='%1.1e')

    # columns of Action mean: x_1*x_4^2, x_2*x_4^2, x_3*x_4^2, x_4^3, x_1, x_2, x_3, x_4
    action = np.zeros((8, 8))
    action[0:4, :] = -elimination[121:125, 141:]
    action[4:, :4] = np.eye(4)

    # np.set_printoptions(precision=4, linewidth=800)
    # print(action)
    # exit(0)
    return action


def build_phi(x):
    x1, x2, x3 = x
    return np.asarray(
        [
            [x1, x1, -x1, -x1, 0.0, 2.0 * x3, -2.0 * x2, 2.0 * x2, 2.0 * x3, 0.0],
            [x2, -x2, x2, -x2, -2.0 * x3, 0.0, 2.0 * x1, 2.0 * x1, 0.0, 2.0 * x3],
            [x3, -x3, -x3, x3, 2.0 * x2, -2.0 * x1, 0.0, 0.0, 2.0 * x1, 2.0 * x2],
        ],
        dtype=np.float64,
    )


def quat2rot(q):
    R = np.eye(3)
    R[0, 0] = pow(q[0], 2) + pow(q[1], 2) - pow(q[2], 2) - pow(q[3], 2)
    R[0, 1] = 2.0 * (q[1] * q[2] - q[0] * q[3])
    R[0, 2] = 2.0 * (q[1] * q[3] + q[0] * q[2])
    R[1, 0] = 2.0 * (q[1] * q[2] + q[0] * q[3])
    R[1, 1] = pow(q[0], 2) - pow(q[1], 2) + pow(q[2], 2) - pow(q[3], 2)
    R[1, 2] = 2.0 * (q[2] * q[3] - q[0] * q[1])
    R[2, 0] = 2.0 * (q[1] * q[3] - q[0] * q[2])
    R[2, 1] = 2.0 * (q[2] * q[3] + q[0] * q[1])
    R[2, 2] = pow(q[0], 2) - pow(q[1], 2) - pow(q[2], 2) + pow(q[3], 2)
    return R


def gauss_jordan(M_, exit_row):
    M = M_.copy()
    precision = 1.0e-10
    rows, cols = M.shape

    ops = []

    for frontrow in range(rows):

        # Find the row that has the biggest leading coefficient
        submatrix = np.abs(M[frontrow:, frontrow])
        maxrow = np.unravel_index(np.argmax(submatrix), submatrix.shape)

        # Account for submatrix shift from 0
        pivot_row = maxrow[0] + frontrow

        E = np.eye(rows, dtype=np.float64)
        E[pivot_row, pivot_row], E[frontrow, frontrow] = 0, 0
        E[pivot_row, frontrow], E[frontrow, pivot_row] = 1, 1

        # Now swap the maxrow that should go in the place of frontRow
        # Numpy slicing doesn't copy, so use tuple indexing
        M[[pivot_row, frontrow]] = M[[frontrow, pivot_row]]

        # First divide all coefficients by the leading coefficient
        E[frontrow, frontrow:] /= M[frontrow, frontrow]
        M[frontrow, frontrow:] /= M[frontrow, frontrow]

        ops.append(E)

        # Determine all columns in frontrow that need manipulation
        nnz = np.abs(M[frontrow]) > precision

        # iterate through all remaining rows, and subtract correct multiple of
        # first row (if leading coefficient is non-zero!)
        E = np.eye(rows, dtype=np.float64)
        for row in range(frontrow + 1, rows):
            leadingCoeff = M[row, frontrow]
            if abs(leadingCoeff) > precision:
                M[row, nnz] -= leadingCoeff * M[frontrow, nnz]
                # This E step is a bit unstable due touching all columns...
                E[row, frontrow] = -leadingCoeff
        ops.append(E)

    # finalE = np.eye(rows, dtype=np.float64)
    # for E in ops:
    #    finalE = E @ finalE
    # M = finalE @ M_

    # Now step up from last non-zero row
    for workrow in range(rows - 1, exit_row, -1):

        # Check which columns needs manipulation
        nnz = np.abs(M[workrow, workrow:]) > precision

        E = np.eye(rows, dtype=np.float64)

        # Work down the rows
        for row in range(workrow - 1, exit_row, -1):
            # Get the leading coefficient
            leadingCoeff = M[row, workrow]

            # Subtract the multiplied front-row
            if abs(leadingCoeff) > precision:
                M[row, workrow:][nnz] -= leadingCoeff * M[workrow, workrow:][nnz]
                E[row, workrow:] = -leadingCoeff
        ops.append(E)

    return M


def upnp(obj_points, proj_points, K):

    # Build bearing vectors: homogenize, inverse K, and normalize!
    K_inv = np.linalg.inv(K)
    N = len(proj_points)
    homo_points = np.hstack((proj_points, np.ones((N, 1)))).astype(np.float64)
    bearing = (K_inv @ homo_points.T).T
    bearing = (bearing.T / np.linalg.norm(bearing, axis=1)).T

    # Bearing line projection
    F = np.zeros((3, 3), dtype=np.float64)
    for f in bearing:
        F += np.outer(f, f)

    # H matrices
    H_inv = (len(bearing) * np.eye(3)) - F
    H = np.linalg.inv(H_inv)

    # I and J matrices
    I = np.zeros((3, 10), dtype=np.float64)
    J = np.zeros(3, dtype=np.float64)
    for f, x in zip(bearing, obj_points):
        V = np.matmul(H, (np.outer(f, f) - np.eye(3)))

        # Build phi matrix from 3D object point x
        phi = build_phi(x)
        I += np.matmul(V, phi)
        # J += V * v   # We don't have camera offsets for the absolute case

    M = np.zeros((10, 10))
    # We solve the centralized absolute pose, so C and gamma stays 0
    C = np.zeros((1, 10))
    gamma = 0

    for f, x in zip(bearing, obj_points):
        phi = build_phi(x)
        temp = np.outer(f, f) - np.eye(3)
        Ai = np.matmul(temp, phi + I)

        M += np.matmul(Ai.T, Ai)
        # bi = np.asarray([0, 0, 0])  # We dont have camera offsets
        # C += np.matmul(bi.T, Ai)
        # gamma += np.matmul(bi.T, bi)

    # Now we run the symmetric case for n > 4 points
    action = setup_action_matrix_symm(M)

    # Decompose action matrix to return all 8 possible rotational solutions
    w, v = np.linalg.eig(action)

    # Now parse out the solutions
    transforms = []
    for i in range(8):

        # Get unscaled quaternion and normalize
        q = np.real(v[4:8, i])
        q /= np.linalg.norm(q)

        s = np.asarray([
            q[0]**2,
            q[1]**2,
            q[2]**2,
            q[3]**2,
            q[0] * q[1],
            q[0] * q[2],
            q[0] * q[3],
            q[1] * q[2],
            q[1] * q[3],
            q[2] * q[3],
        ])

        # print(q)

        # Compute object space error of solution (should be way below 1)
        valueM = (s.T @ M) @ s + 2.0 * C @ s
        value = valueM[0] + gamma

        # Build the rotation and translation
        R = quat2rot(q)
        t = (I @ s) - J

        # Chirality test
        chirality = 0
        for p, b in zip(((R @ obj_points.T).T + t), bearing):
            if p @ b < 0:
                chirality += 1

        transforms.append([value, R, t, chirality])

    # Sort all results with ascending error
    transforms.sort(key=lambda tup: tup[0])
    return transforms
